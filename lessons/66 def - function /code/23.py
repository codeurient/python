# Funksiyani cagirdiqda yaninda () yumru moterizeler yazilir. Ancaq funksiyani her hansisa bir variable-la 
# verdikde diqqet edin cunki hemin yumru moterizeler yazilmir. Ancaq o variable-i cagirqida yeniden yumru 
# moterize yazmaq lazimdir yoxsa funksiya islemez. 

def numune(n):
  return n * n

# Niye gore burda funksiyani variable-a verdikde yumru moterize yazmiriq? cunki yumru moterize qoyanda 
# funksiya iwlemeye bawlayir ve xeta aliriq. 
vurma = numune(22)

print(    vurma(11)    )