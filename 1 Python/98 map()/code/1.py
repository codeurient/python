# map() - bu funksiya 2 parametr qebul edir: map(function, iterable)
# 1ci parametri funksiyadir.
# 2ci parametri ise iterabe - yeni tekrarlana bilen obyektler.

# Tekrarlana bilen obyektlere aiddir neler? 
# 1. list   -  [1, 2, 3]
# 2. tuple  -  (1, 2, 3)
# 3. set    -  {1, 2, 3}
# 4. dict   -  {a:1,    b:2,    c:3}
# 5. str    -  "Hello"

# Iterable obyektlerin icindeki deyerleri bir-bir elde etmek ucun 'for' dongusunden istifade edilir.
# Elde edilen deyerler ile ferqli iwler gormek mumkundur.

