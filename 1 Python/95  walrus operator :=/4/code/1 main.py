# Burda ilk once "x" deyiskenine 10 deyerini veririk. Sonra ise "10 > 5" deyerek
# if sorgusu ile muqayise edirik. Eger boyukdurse True qayidacaq ve hemin "x" variable-inin 
# deyeri ekrana yazdirilacaq.
x = 10
if x > 5:
    print(x)  



#! Yuxarida yazdigimiz ile eyni numunedir bu. Sadece daha qisa ve kompaktdir.
#! Yene "x" variable-ina 10 deyerini veririk ve 5 ile muqaiyes edirik. Sorgu True olarsa
#! ekrana neticeni yazdiririq.
if (x := 10) > 5:
    print(x) 

